    private void addData() {
        Total.setText(String.valueOf(this.Cart.getTotal()) + "  VND");
        Cart.setgrantTotal();
        Total2.setText(String.valueOf(this.Cart.getGrantTotal()) + "  VND");
        model = (DefaultTableModel) table1.getModel();
        model.setRowCount(0);
        int rowNum = 1;
        for (ModelGame game : Cart.getGames()) {
            String gameName = game.getGameName();
            double price = game.getPrice();
            model.addRow(new Object[]{rowNum++,gameName,price});
        }
        
        
    }
